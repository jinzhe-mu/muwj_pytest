# 字符串的引用 %s
name = 'muwj'
print("my name is %s" % name)
# 十进制整数的引用 %d
age = 3
print("my name is %s, my age is %d" % (name, age))
# 浮点数的引用 %f
weight = 55.44
print("my name is %s, my age is %d, my weigh is %f" % (name, age, weight))
print("my name is %s, my age is %d, my weigh is %.2f" % (name, age, weight))

